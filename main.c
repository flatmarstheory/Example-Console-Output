#include <stdio.h>
int main() {
  // "Good morning" ਪ੍ਰਿੰਟ ਕਰੋ, ਨਵੀਂ ਲਾਈਨ 'ਤੇ ਜਾਓ, ਫਿਰ "Ms Smith." ਪ੍ਰਿੰਟ ਕਰੋ ਅਤੇ ਫਿਰ ਇੱਕ
  // ਹੋਰ ਨਵੀਂ ਲਾਈਨ 'ਤੇ ਜਾਓ
  printf("Good morning\nMs Smith.\n");
  int age = 25; // age ਵੇਰੀਏਬਲ ਨੂੰ 25 ਦਾ ਮੁੱਲ ਦਿਓ
  printf("Age is %d\n", age); // "Age is " ਪ੍ਰਿੰਟ ਕਰੋ ਅਤੇ ਫਿਰ age ਦਾ ਮੁੱਲ ਪ੍ਰਿੰਟ ਕਰੋ

  return 0;
}